public class Matematika {
    
    // Mendefinisikan method tambah
    public int tambah(int a, int b) {
        return a + b;
    }

    // Mendefinisikan method kurang
    public int kurang(int a, int b) {
        return a - b;
    }

    // Mendefinisikan method kali
    public int kali(int a, int b) {
        return a * b;
    }

    // Mendefinisikan method bagi
    public int bagi(int a, int b) {
        return a / b;
    }
    
}